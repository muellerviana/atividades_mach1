/**
Math.abs(n) = converte em valor absoluto(ex.numero negativo remove o sinal)
Math.ceil(n) = arredonda o valor para cima
Math.floor(n) = arredonda o valor para baixo
Math.pow(n, exp) = elevar ao expoente
Math.random() = retorna um numero aleatorio entre 0 e 1
Math.round(n) = arredonda o valor para o inteiro mais proximo >= 0.5 é arredondado para cima
x.toFixed(n) = fixa a quantidade de caracteres a ser exibida após o ponto
 */

const valor = 20.55;
console.log(Math.abs(valor));
console.log(Math.ceil(valor));
console.log(Math.floor(valor));
console.log(Math.pow(valor));
console.log(Math.random());
console.log(Math.round(valor));
console.log(valor.toFixed(1));